#include "gwen/model.h"
#include "gwen/inference.h"
#include "gwen/options.h"
#include "gwen/paths.h"
#include "gwen/tokenizer.h"

#include <chrono>
#include <future>

using namespace gwen;

int main(int argc, char** argv) {
    auto opts = Options::parse(argc, argv);

    // Apply model path default + auto-download
    if (opts.model_path.empty()) opts.model_path = gwen::default_model_path();
    gwen::ensure_file(opts.model_path,
                      (std::string(gwen::RELEASE_BASE) + "/" + gwen::DEFAULT_MODEL).c_str());

    // Load model
    auto model = Model::load(opts.model_path);

    if (opts.info_only) {
        model->print_info();
        return 0;
    }

    // Build tokenizer concurrently with weight upload
    auto tok_future = std::async(std::launch::async, [&]() {
        return Tokenizer::from_gguf(*model->gguf);
    });

    CudaAllocator allocator;
    model->upload_weights(allocator);
    auto tokenizer = tok_future.get();

    if (opts.input_text.empty()) {
        fprintf(stderr, "\nNo input text. Pass text as positional argument.\n");
        return 0;
    }

    // Build prompt and tokenize
    std::string prompt = opts.build_prompt();
    auto prompt_tokens = tokenizer->encode(prompt);

    if (opts.debug) {
        fprintf(stderr, "Prompt tokens (%zu): ", prompt_tokens.size());
        for (size_t i = 0; i < prompt_tokens.size(); i++) fprintf(stderr, "%d ", prompt_tokens[i]);
        fprintf(stderr, "\n");
        printf("%s", prompt.c_str());
        fflush(stdout);
    }

    // Allocate inference state
    InferenceState state;
    state.allocate(model->config, allocator);
    state.allocate_prefill(model->config, allocator, 4096);

    // Stream tokens вҖ” in debug mode print everything, otherwise skip think block and stop tokens
    bool in_think = opts.reason && !opts.raw && !opts.debug;
    std::string think_buf;
    auto on_token = [&](int token_id) {
        std::string text = tokenizer->decode(token_id);
        if (in_think) {
            think_buf += text;
            static constexpr const char* THINK_CLOSE = "</think>";
            auto pos = think_buf.find(THINK_CLOSE);
            if (pos != std::string::npos) {
                in_think = false;
                std::string after = think_buf.substr(pos + strlen(THINK_CLOSE));
                size_t start = after.find_first_not_of("\n\r");
                if (start != std::string::npos) {
                    printf("%s", after.substr(start).c_str());
                    fflush(stdout);
                }
            }
        } else if (token_id != (int)model->config.eos_token_id &&
                   token_id != (int)model->config.eot_token_id) {
            printf("%s", text.c_str());
            fflush(stdout);
        }
    };

    // Build sampling params based on mode
    auto sampling = opts.greedy  ? SamplingParams::greedy_mode() :
                    opts.reason  ? SamplingParams::qwen_thinking() :
                                   SamplingParams::qwen_default();

    if (opts.debug) {
        if (sampling.greedy) {
            fprintf(stderr, "Sampling: greedy\n");
        } else {
            fprintf(stderr, "Sampling: temp=%.1f top_k=%d top_p=%.2f presence_penalty=%.1f\n",
                    sampling.temperature, sampling.top_k, sampling.top_p, sampling.presence_penalty);
        }
    }

    auto t0 = std::chrono::high_resolution_clock::now();

    auto output_tokens = state.generate(*model, prompt_tokens, opts.n_predict,
                                        sampling, opts.teacher_tokens, on_token);

    GWEN_CHECK_CUDA(cudaDeviceSynchronize());
    auto t1 = std::chrono::high_resolution_clock::now();

    printf("\n");

    // Stats on stderr
    double gen_ms = std::chrono::duration<double, std::milli>(t1 - t0).count();
    int n_gen = (int)output_tokens.size();
    double tok_per_s = n_gen / (gen_ms / 1000.0);
    fprintf(stderr, "[%d tokens, %.1f tok/s, %.1f ms]\n", n_gen, tok_per_s, gen_ms);

    return 0;
}
