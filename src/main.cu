static void print_usage(const char* prog) {
    fprintf(stderr, "Usage: %s [options]\n", prog);
    fprintf(stderr, "Options:\n");
    fprintf(stderr, "  --model PATH         Path to GGUF model file (default: ~/.cache/gwen/%s)\n", gwen::DEFAULT_MODEL);
    fprintf(stderr, "  --mtp PATH           Path to MTP weights file (default: ~/.cache/gwen/%s)\n", gwen::DEFAULT_MTP);
    fprintf(stderr, "  --no-mtp             Disable MTP speculative decoding\n");
    fprintf(stderr, "  --template STR/FILE  Template (file or string); {input} replaced with input text\n");
    fprintf(stderr, "  --max-predict N        Number of tokens to generate (default: 50)\n");
    fprintf(stderr, "  --greedy             Use greedy decoding\n");
    fprintf(stderr, "  --mtp-threshold F    Confidence threshold for MTP (skip if softmax prob < F)\n");
    fprintf(stderr, "  --benchmark          Output benchmark timing as JSON\n");
    fprintf(stderr, "  --output-logits      Output raw logits\n");
    fprintf(stderr, "  --teacher-tokens T   Comma-separated reference token IDs for teacher-forced comparison\n");
    fprintf(stderr, "  --batch-extract FILE Batch GEMM extract mode: read prompts from FILE (one per line)\n");
    fprintf(stderr, "  --seq-len N          Sequence length for batch-extract (pad/truncate, default: 512)\n");
    fprintf(stderr, "  --compare-extract    Compare extract_hidden (F32 GEMV) vs extract_hidden_batch (B=1 GEMM)\n");
    fprintf(stderr, "  --profile-cycles     Profile per-operation CUDA timing in speculative decode\n");
    fprintf(stderr, "  --info               Print model info and exit\n");
    fprintf(stderr, "  --help               Show this help\n");
}

int main(int argc, char** argv) {
    std::string model_path;
    std::string mtp_path;
    bool no_mtp = false;
    std::string tmpl_path;
    std::string teacher_tokens_str;
    std::string batch_extract_file;
    int n_predict = 50;
    float mtp_threshold = 0.0f;
    bool greedy = true;  // default greedy for now
    bool benchmark = false;
    bool output_logits = false;
    bool info_only = false;
    bool compare_extract = false;
    bool profile_cycles = false;
    int seq_len = 512;

    static struct option long_options[] = {
        {"model",        required_argument, nullptr, 'm'},
        {"mtp",          required_argument, nullptr, 'M'},
        {"no-mtp",       no_argument,       nullptr, 'N'},
        {"mtp-threshold",required_argument, nullptr, 'T'},
        {"template",     required_argument, nullptr, 'p'},
        {"max-predict",    required_argument, nullptr, 'n'},
        {"greedy",       no_argument,       nullptr, 'g'},
        {"benchmark",    no_argument,       nullptr, 'b'},
        {"output-logits",no_argument,       nullptr, 'l'},
        {"teacher-tokens",required_argument, nullptr, 't'},
        {"batch-extract",required_argument, nullptr, 'B'},
        {"seq-len",      required_argument, nullptr, 'S'},
        {"compare-extract", no_argument,    nullptr, 'C'},
        {"profile-cycles", no_argument,    nullptr, 'P'},
        {"info",         no_argument,       nullptr, 'i'},
        {"help",         no_argument,       nullptr, 'h'},
        {nullptr, 0, nullptr, 0},
    };

    int opt;
    while ((opt = getopt_long(argc, argv, "m:M:NT:p:n:t:B:S:CPgblih", long_options, nullptr)) != -1) {
        switch (opt) {
            case 'm': model_path = optarg; break;
            case 'M': mtp_path = optarg; break;
            case 'N': no_mtp = true; break;
            case 'T': mtp_threshold = atof(optarg); break;
            case 'p': tmpl_path = optarg; break;
            case 'n': n_predict = atoi(optarg); break;
            case 't': teacher_tokens_str = optarg; break;
            case 'g': greedy = true; break;
            case 'b': benchmark = true; break;
            case 'l': output_logits = true; break;
            case 'B': batch_extract_file = optarg; break;
            case 'S': seq_len = atoi(optarg); break;
            case 'C': compare_extract = true; break;
            case 'P': profile_cycles = true; break;
            case 'i': info_only = true; break;
            case 'h': print_usage(argv[0]); return 0;
            default:  print_usage(argv[0]); return 1;
        }
    }

    (void)output_logits;

    // Positional arguments: [model_path] [input_text]
    // If first positional arg looks like a model file (ends in .gguf/.gwfp8/.gwfp4), treat it as model path
    std::string input_text;
    auto ends_with = [](const std::string& s, const std::string& suffix) {
        return s.size() >= suffix.size() && s.substr(s.size() - suffix.size()) == suffix;
    };
    for (int i = optind; i < argc; i++) {
        std::string arg = argv[i];
        if (model_path.empty() && (ends_with(arg, ".gguf") || ends_with(arg, ".gwfp8") ||
                                    ends_with(arg, ".gwfp4"))) {
            model_path = arg;
        } else {
            if (!input_text.empty()) input_text += " ";
            input_text += arg;
        }
    }

    // Apply defaults from ~/.cache/gwen/ (auto-download if needed)
    if (model_path.empty()) model_path = gwen::default_model_path();
    if (mtp_path.empty() && !no_mtp && !ends_with(model_path, ".gguf")) mtp_path = gwen::default_mtp_path();

    gwen::ensure_file(model_path, (std::string(gwen::RELEASE_BASE) + "/" + gwen::DEFAULT_MODEL).c_str());
    if (!mtp_path.empty()) {
        gwen::ensure_file(mtp_path, (std::string(gwen::RELEASE_BASE) + "/" + gwen::DEFAULT_MTP).c_str());
    }

    // Load model
    std::unique_ptr<Model> model = Model::load(model_path);

    // Load MTP weights (default: on, disable with --no-mtp)
    if (!mtp_path.empty()) {
        model->load_mtp(mtp_path);
    }

    if (info_only) {
        model->print_info();
        return 0;
    }

    // Build tokenizer concurrently with weight upload.
    // For GWFP8/GWFP4: load tokenizer from the default GGUF (metadata only, no weight upload)
    // TODO: embed tokenizer in binary format or load from HF tokenizer.json
    std::unique_ptr<GGUFFile> tok_gguf;
    if (!model->gguf) {
        tok_gguf = GGUFFile::open(gwen::default_model_path());
    }
    const GGUFFile& tok_source = model->gguf ? *model->gguf : *tok_gguf;
    auto tok_future = std::async(std::launch::async, [&]() {
        return Tokenizer::from_gguf(tok_source);
    });

    CudaAllocator allocator;
    model->upload_weights(allocator);

    auto tokenizer = tok_future.get();

    // ========== Compare extract mode ==========
    if (compare_extract) {
        // Compare single-seq extract_hidden (F32 GEMV) vs batch extract_hidden_batch (B=1 GEMM)
        const char* test_prompts[] = {
            "The quick brown fox jumps over the lazy dog.",
            "In the beginning, there was nothing but silence.",
            "def fibonacci(n):\n    if n <= 1:\n        return n",
        };
        int n_prompts = 3;
        int L = seq_len;
        int n_embed = model->config.n_embed;

        fprintf(stderr, "\n=== Compare Extract: F32 GEMV (extract_hidden) vs FP16 GEMM (extract_hidden_batch, B=1) ===\n");
        fprintf(stderr, "Seq length: %d, n_embed: %d\n\n", L, n_embed);

        // Allocate inference state + batch prefill buffers
        InferenceState state;
        state.allocate(model->config, allocator);
        state.allocate_batch_prefill(model->config, allocator, L, 1);
        fprintf(stderr, "Total GPU memory: %.1f MB\n\n", allocator.total_allocated() / 1024.0 / 1024.0);

        int all_pass = 1;
        for (int p = 0; p < n_prompts; p++) {
            auto tokens = tokenizer->encode(test_prompts[p]);

            // Pad/truncate to L
            std::vector<int> tok_padded(L, 0);
            int copy_len = std::min((int)tokens.size(), L);
            for (int j = 0; j < copy_len; j++) tok_padded[j] = tokens[j];
            int actual_len = copy_len;

            fprintf(stderr, "[%d] \"%s\" (%d tokens, padded to %d)\n", p, test_prompts[p], (int)tokens.size(), L);

            // Run F32 GEMV path (extract_hidden)
            std::vector<uint16_t> gemv_out((size_t)L * n_embed);
            state.extract_hidden(*model, tok_padded, gemv_out.data());

            // Run FP16 GEMM batch path (extract_hidden_batch, B=1)
            std::vector<int32_t> tok_i32(tok_padded.begin(), tok_padded.end());
            std::vector<uint16_t> gemm_out((size_t)L * n_embed);
            state.extract_hidden_batch(*model, tok_i32.data(), 1, L, gemm_out.data());

            // Compare per position
            int bit_mismatches = 0;
            float max_abs = 0.0f;
            double sum_abs = 0.0;
            for (int t = 0; t < actual_len; t++) {
                float cos_num = 0, cos_den_a = 0, cos_den_b = 0;
                float pos_max_abs = 0;
                for (int d = 0; d < n_embed; d++) {
                    size_t idx = (size_t)t * n_embed + d;
                    uint16_t a_bits = gemv_out[idx], b_bits = gemm_out[idx];
                    if (a_bits != b_bits) bit_mismatches++;
                    // Convert to float for comparison
                    half a_h, b_h;
                    memcpy(&a_h, &a_bits, 2);
                    memcpy(&b_h, &b_bits, 2);
                    float a = __half2float(a_h), b = __half2float(b_h);
                    float diff = fabsf(a - b);
                    if (diff > pos_max_abs) pos_max_abs = diff;
                    sum_abs += diff;
                    cos_num += a * b;
                    cos_den_a += a * a;
                    cos_den_b += b * b;
                }
                float cos_sim = cos_num / (sqrtf(cos_den_a) * sqrtf(cos_den_b) + 1e-12f);
                if (pos_max_abs > max_abs) max_abs = pos_max_abs;

                if (t < 5 || t == actual_len - 1) {
                    fprintf(stderr, "  pos[%3d]: cos=%.6f  max_abs=%.6f\n", t, cos_sim, pos_max_abs);
                } else if (t == 5) {
                    fprintf(stderr, "  ...\n");
                }

                if (cos_sim < 0.9999f) {
                    fprintf(stderr, "  WARN: low cosine at pos %d: %.6f\n", t, cos_sim);
                    all_pass = 0;
                }
            }
            size_t total_elems = (size_t)actual_len * n_embed;
            float mean_abs = (float)(sum_abs / total_elems);
            fprintf(stderr, "  Summary: bit_mismatches=%d/%zu  max_abs=%.6f  mean_abs=%.6f\n",
                   bit_mismatches, total_elems, max_abs, mean_abs);

            // F32 vs FP16 paths: max_abs up to ~0.01 is expected (FP16 precision)
            // Cosine < 0.9999 would indicate actual corruption
            if (max_abs > 0.05f) {
                fprintf(stderr, "  FAIL: max_abs > 0.05 (likely corruption)\n");
                all_pass = 0;
            } else {
                fprintf(stderr, "  PASS\n");
            }
            fprintf(stderr, "\n");
        }

        fprintf(stderr, "=== %s ===\n", all_pass ? "ALL PASSED" : "SOME FAILED");
        return all_pass ? 0 : 1;
    }

    // ========== Batch extract mode ==========
    if (!batch_extract_file.empty()) {
        // Read prompts from file (one per line)
        std::ifstream infile(batch_extract_file);
        if (!infile.is_open()) {
            fprintf(stderr, "Error: cannot open %s\n", batch_extract_file.c_str());
            return 1;
        }
        std::vector<std::string> prompts;
        std::string line;
        while (std::getline(infile, line)) {
            if (!line.empty()) prompts.push_back(line);
        }
        if (prompts.empty()) {
            fprintf(stderr, "Error: no prompts in %s\n", batch_extract_file.c_str());
            return 1;
        }

        int B = (int)prompts.size();
        int L = seq_len;
        fprintf(stderr, "\nBatch extract mode: B=%d, L=%d (from %s)\n", B, L, batch_extract_file.c_str());

        // Tokenize each prompt, pad/truncate to L
        std::vector<int32_t> all_tokens(B * L, 0);
        for (int b = 0; b < B; b++) {
            auto tokens = tokenizer->encode(prompts[b]);
            int copy_len = std::min((int)tokens.size(), L);
            for (int j = 0; j < copy_len; j++) {
                all_tokens[b * L + j] = tokens[j];
            }
            fprintf(stderr, "  [%d] %zu tok%s — %.40s%s\n", b, tokens.size(),
                   (int)tokens.size() > L ? " (truncated)" :
                   (int)tokens.size() < L ? " (padded)" : "",
                   prompts[b].c_str(),
                   prompts[b].size() > 40 ? "..." : "");
        }

        // Allocate batch state
        fprintf(stderr, "\nAllocating batch state...\n");
        InferenceState state;
        state.allocate(model->config, allocator);
        state.allocate_batch_prefill(model->config, allocator, B * L, B);
        fprintf(stderr, "Total GPU memory: %.1f MB\n", allocator.total_allocated() / 1024.0 / 1024.0);

        // Output buffer
        int n_embed = model->config.n_embed;
        std::vector<uint16_t> output((size_t)B * L * n_embed);

        // Run extraction
        fprintf(stderr, "\nRunning batch extraction...\n");
        auto t4 = std::chrono::high_resolution_clock::now();

        state.extract_hidden_batch(*model, all_tokens.data(), B, L, output.data());

        GWEN_CHECK_CUDA(cudaDeviceSynchronize());
        auto t5 = std::chrono::high_resolution_clock::now();
        double ms = std::chrono::duration<double, std::milli>(t5 - t4).count();
        int total_tokens = B * L;
        double tok_per_s = total_tokens / (ms / 1000.0);

        fprintf(stderr, "\n--- Batch Extract Timing ---\n");
        fprintf(stderr, "Batch size: %d\n", B);
        fprintf(stderr, "Seq length: %d\n", L);
        fprintf(stderr, "Total tokens: %d\n", total_tokens);
        fprintf(stderr, "Time: %.1f ms\n", ms);
        fprintf(stderr, "Throughput: %.0f tok/s\n", tok_per_s);

        if (benchmark) {
            fprintf(stderr,
                "{\"batch\": %d, \"seq_len\": %d, \"total_tokens\": %d, "
                "\"total_ms\": %.2f, \"tok_per_s\": %.2f, "
                "\"peak_vram_mb\": %.1f}\n",
                B, L, total_tokens, ms, tok_per_s,
                allocator.total_allocated() / 1024.0 / 1024.0);
        }

        return 0;
    }

    // ========== Normal generation mode ==========
    if (input_text.empty()) {
        fprintf(stderr, "\nNo input text. Pass text as positional argument.\n");
        return 0;
    }

    // Build prompt:
    //   no template   → input text as-is
    //   template w/o {input} → template + input text
    //   template w/ {input}  → {input} replaced with input text
    std::string prompt;
    if (!tmpl_path.empty()) {
        // Try as file first, fall back to literal string
        std::string tmpl;
        std::ifstream tf(tmpl_path);
        if (tf.is_open()) {
            tmpl = std::string((std::istreambuf_iterator<char>(tf)),
                                std::istreambuf_iterator<char>());
        } else {
            tmpl = tmpl_path;
        }
        auto pos = tmpl.find("{input}");
        if (pos != std::string::npos) {
            tmpl.replace(pos, 7, input_text);
        } else {
            tmpl += input_text;
        }
        prompt = tmpl;
    } else {
        prompt = input_text;
    }

    // Tokenize prompt
    auto prompt_tokens = tokenizer->encode(prompt);
    fprintf(stderr, "Prompt tokens (%zu): ", prompt_tokens.size());
    for (size_t i = 0; i < prompt_tokens.size(); i++) fprintf(stderr, "%d ", prompt_tokens[i]);
    fprintf(stderr, "\n");

    // Allocate inference state
    InferenceState state;
    state.allocate(model->config, allocator);
    state.allocate_prefill(model->config, allocator, 4096);
    if (model->has_mtp) {
        state.allocate_mtp(model->config, allocator, 4096);
        state.allocate_batch2(model->config, allocator);
        state.mtp_confidence_threshold = mtp_threshold;
        state.profile_cycles = profile_cycles;
    }

    // Parse teacher tokens if provided
    std::vector<int> teacher_tokens;
    if (!teacher_tokens_str.empty()) {
        std::istringstream iss(teacher_tokens_str);
        std::string tok;
        while (std::getline(iss, tok, ',')) {
            if (!tok.empty()) teacher_tokens.push_back(std::stoi(tok));
        }
    }

    // Generate
    auto t0 = std::chrono::high_resolution_clock::now();

    std::vector<int> output_tokens;
    if (!teacher_tokens.empty()) {
        output_tokens = state.generate(*model, prompt_tokens, n_predict, greedy, 1.0f, teacher_tokens);
    } else if (model->has_mtp) {
        output_tokens = state.generate_speculative(*model, prompt_tokens, n_predict);
    } else {
        output_tokens = state.generate(*model, prompt_tokens, n_predict, greedy);
    }

    GWEN_CHECK_CUDA(cudaDeviceSynchronize());
    auto t1 = std::chrono::high_resolution_clock::now();

    // Output: generated text on stdout
    std::string output_text = tokenizer->decode(output_tokens);
    printf("%s%s\n", prompt.c_str(), output_text.c_str());

    // Compact stats on stderr
    double gen_ms = std::chrono::duration<double, std::milli>(t1 - t0).count();
    int n_gen = (int)output_tokens.size();
    double tok_per_s = n_gen / (gen_ms / 1000.0);
    fprintf(stderr, "[%d tokens, %.1f tok/s, %.1f ms]\n", n_gen, tok_per_s, gen_ms);

    if (benchmark) {
        int n_prompt = (int)prompt_tokens.size();
        fprintf(stderr,
            "{\"prompt_tokens\": %d, \"decode_tokens\": %d, "
            "\"total_ms\": %.2f, \"decode_tok_per_s\": %.2f, "
            "\"ttft_ms\": %.2f, \"peak_vram_mb\": %.1f}\n",
            n_prompt, n_gen, gen_ms, tok_per_s,
            gen_ms / (n_prompt + n_gen) * n_prompt,
            allocator.total_allocated() / 1024.0 / 1024.0);
    }

    return 0;
}
